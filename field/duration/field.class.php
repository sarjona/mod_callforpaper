    function generate_sql($tablealias, $value) {
        global $DB;

        static $i=0;
        $i++;
        $name = "df_date_$i";
        $varcharcontent = $DB->sql_compare_text("{$tablealias}.content");
        return array(" ({$tablealias}.fieldid = {$this->field->id} AND $varcharcontent = :$name) ", array($name => $value['timestamp']));
    }

    public function parse_search_field($defaults = null) {
        $paramday = 'f_'.$this->field->id.'_d';
        $parammonth = 'f_'.$this->field->id.'_m';
        $paramyear = 'f_'.$this->field->id.'_y';
        $paramusedate = 'f_'.$this->field->id.'_z';
        if (empty($defaults[$paramday])) {  // One empty means the other ones are empty too.
            $defaults = array($paramday => 0, $parammonth => 0, $paramyear => 0, $paramusedate => 0);
        }

        $day   = optional_param($paramday, $defaults[$paramday], PARAM_INT);
        $month = optional_param($parammonth, $defaults[$parammonth], PARAM_INT);
        $year  = optional_param($paramyear, $defaults[$paramyear], PARAM_INT);
        $usedate = optional_param($paramusedate, $defaults[$paramusedate], PARAM_INT);

        $callforpaper = array();
        if (!empty($day) && !empty($month) && !empty($year) && $usedate == 1) {
            $calendartype = \core_calendar\type_factory::get_calendar_instance();
            $gregoriandate = $calendartype->convert_to_gregorian($year, $month, $day);

            $callforpaper['timestamp'] = make_timestamp(
                $gregoriandate['year'],
                $gregoriandate['month'],
                $gregoriandate['day'],
                $gregoriandate['hour'],
                $gregoriandate['minute'],
                0,
                0,
                false);
            $callforpaper['usedate'] = 1;
            return $callforpaper;
        } else {
            return 0;
        }
    }

    function update_content($recordid, $value, $name = '') {
        global $DB;

        $names = explode('_', $name);
        if (count($names) < 3) {
            return false;
        }
        $name = $names[2];          // day month or year

        $this->$name = $value;

        if (!$this->day || !$this->month || !$this->year) {
            return false;
        }

        $content = new stdClass();
        $content->fieldid = $this->field->id;
        $content->recordid = $recordid;

        $calendartype = \core_calendar\type_factory::get_calendar_instance();
        $gregoriandate = $calendartype->convert_to_gregorian(
            $this->year,
            $this->month,
            $this->day,
            $this->fromhour ?? 0,
            $this->fromminute ?? 0,
        );
        $content->content = make_timestamp(
            $gregoriandate['year'],
            $gregoriandate['month'],
            $gregoriandate['day'],
            $gregoriandate['hour'],
            $gregoriandate['minute'],
            0,
            0,
            false);
        $gregoriandate = $calendartype->convert_to_gregorian(
            $this->year,
            $this->month,
            $this->day,
            $this->tohour ?? 0,
            $this->tominute ?? 0,
        );
        $content->content1 = make_timestamp(
            $gregoriandate['year'],
            $gregoriandate['month'],
            $gregoriandate['day'],
            $gregoriandate['hour'],
            $gregoriandate['minute'],
            0,
            0,
            false);

        if ($oldcontent = $DB->get_record('callforpaper_content', array('fieldid'=>$this->field->id, 'recordid'=>$recordid))) {
            $content->id = $oldcontent->id;
            return $DB->update_record('callforpaper_content', $content);
        } else {
            return $DB->insert_record('callforpaper_content', $content);
        }
    }

    function display_browse_field($recordid, $template) {
        $content = $this->get_callforpaper_content($recordid);
        if (!$content || empty($content->content)) {
            return '';
        }

        $date = userdate($content->content, get_string('strftimedate'), 0);
        $starttime = userdate($content->content, get_string('strftimetime24', 'core_langconfig'), 0);
        $endtime = userdate($content->content1, get_string('strftimetime24', 'core_langconfig'), 0);
        return get_string('displayduration', 'callforpaperfield_duration', [
            'date' => $date,
            'starttime' => $starttime,
            'endtime' => $endtime,
        ]);
    }

    function get_sort_sql($fieldname) {
        global $DB;
        return $DB->sql_cast_char2real($fieldname, true);
    }

    /**
     * Return the plugin configs for external functions.
     *
     * @return array the list of config parameters
     * @since Moodle 3.3
     */
    public function get_config_for_external() {
        // Return all the config parameters.
        $configs = [];
        for ($i = 1; $i <= 10; $i++) {
            $configs["param$i"] = $this->field->{"param$i"};
        }
        return $configs;
    }

    function export_text_value($record) {
        return $record->content . " " . $record->content1;
    }

    function update_content_import($recordid, $value, $name = '') {
        global $DB;

        $field = explode('_', $name);
        $fieldid = $field[1];
        if ($fieldid != $this->field->id) {
            return false;
        }

        $contents = explode(' ', $value, 2);
        $content  = $contents[0] ?? 0;
        $content1 = $contents[1] ?? 0;
        if ($existingcontent = $DB->get_record('callforpaper_content', ['fieldid' => $this->field->id, 'recordid' => $recordid])) {
            // Update existing content record.
            $existingcontent->content = $content;
            $existingcontent->content1 = $content1;
            return $DB->update_record('callforpaper_content', $existingcontent);
        }
        // Create new content record.
        $newcontent = new stdClass();
        $newcontent->fieldid = $this->field->id;
        $newcontent->recordid = $recordid;
        $newcontent->content = $content;
        $newcontent->content1 = $content1;
        return $DB->insert_record('callforpaper_content', $newcontent);
    }
